                                <h2 class="crux-intro-large">Getting Ready for Towing</h2>
                                <div class="text-container crux-article">
                                    <ul>
                                        <li><b>Check those trailer tires. </b>"A lot of people check the tire pressures
                                            on their pickup truck, but they forget to check the tires on the trailer,"
                                            Ibbotson says. "Also inspect the trailer tires for dry rot and cracking,
                                            especially if the trailer is stored outside and hasn't been used for
                                            months." Even if the tires appear to have plenty of tread, they age over
                                            time and that can lead to tire failure. Also, your <a
                                                href="https://www.consumerreports.org/tires/tire-safety-checklist-what-to-do-before-road-trip/">tow
                                                vehicle's tires</a> may require a higher pressure for towing, as
                                            outlined in the owner's manual. Don't forget to make sure the wheel lug nuts
                                            on the trailer and tow vehicle are tightened to the specified torque.</li>
                                    </ul>
                                </div>
